"""
DocString
Python = Linguagem de programação
Tipo de tipagem = Dinâmica / Forte
str -> string -> texto
Strings são textos que estão dentro de aspas
"""
print(1234)

# Aspas simples
print('Luiz Otávio')
print(1, 'Luiz "Otávio"') # A maneira mais limpa é intercala a forma das aspas

# Aspas duplas
print("Luiz Otávio")
print(2, "Luiz 'Otávio'") # A maneira mais limpa é intercala a forma das aspas

# Escape (Faz com que o programa não interprete o que vem após o \)
print("Luiz \"Otávio\"")

# r (Utilizado para expressões regulares)
print(r"Luiz \"Otávio\"")